package jrJava.practice6;

public class UsingForLoop {

	public static void main(String[] args) {


		int i;
		
		for(i=1; i<=100; i++){
			
			System.out.println("Sir is handsome!");
			System.out.println("Sir has a wonderful voice!");
			System.out.println("i=" + i);
			System.out.println();
		}
		
		System.out.println("I am out of the loop. i=" + i);
		
	}

}
